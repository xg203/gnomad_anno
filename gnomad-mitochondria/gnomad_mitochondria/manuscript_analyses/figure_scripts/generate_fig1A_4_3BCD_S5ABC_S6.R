##############################################################################
# Redefine upper boundaries for plotting purposes (group values above x into an x+ category)
gnomad <- mutate(gnomad, mt_mean_coverage_for_plotting = ifelse(mt_mean_coverage >= 10000, 10000, mt_mean_coverage))

