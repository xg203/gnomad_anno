        axis.title = element_text(colour = "black", size = 28, face = "bold"),
        panel.grid.major = element_blank(), 
        panel.grid.minor = element_blank()) +
  scale_x_continuous(limits = c(0, 10000), breaks = c(seq(0, 10000, by = 1000)), labels = c(seq(0, 9000, by = 1000), ">10000")) +
  labs(x = "mtDNA mean coverage", y = "Count")  
plot(mean_cov_hist_plot )

setwd(plot_dir)
ggsave(mean_cov_hist_plot, filename = "FigS5A.png", dpi = 300, width = 11, height = 6, units = "in")
ggsave(mean_cov_hist_plot, filename = "FigS5A.pdf", dpi = 300, width = 11, height = 6, units = "in")

##############################################################################
# Plot histogram distribution of mitochondrial copy number estimate (FigS5C)
##############################################################################
cp_hist_plot <- ggplot(gnomad, aes(x = mito_cn)) + 
  geom_histogram(binwidth = 10) +
  theme_bw() +
  theme(axis.text = element_text(size = 20, colour = "black"),
        axis.title = element_text(colour = "black", size = 28, face = "bold"),
        panel.grid.major = element_blank(), 
        panel.grid.minor = element_blank()) +
  scale_x_continuous(limits = c(0, 500), breaks = c(seq(0, 550, by = 100))) +
  labs(x = "mtDNA copy number estimate", y = "Count")  
plot(cp_hist_plot)

