# Make gnomAD metrics dataframe to pull out statistic on number of hets and homs
##############################################################################
# Reformat a new dataframe for the variant metrics
gnomad_metrics <- gather(gnomad, "metric", "value",
                         callrate,
                         mito_cn,
                         n_singletons_het,
                         n_singletons_hom,
                         n_indel_het,
                         n_indel_hom,
                         n_snp_het,
                         n_snp_hom)

